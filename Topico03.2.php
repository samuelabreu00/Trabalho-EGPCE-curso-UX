                            <br><h4 style="color: #465564"><strong>1990 - UX  </strong></h4>
                            <hr>

                            <br><p>Don Norman popularizou o termo “<strong>user experience</strong>” enquanto trabalhava na Apple nos anos 90. Recentemente Norman falou sobre o <a href="https://www.youtube.com/watch?v=9BdtGjoIN4E&feature=youtu.be" target="_blank">termo UX</a> em um vídeo da NNGroup e relembrou de como a experiência de utilizar um computador era fraca, complexa e chegava até a dar medo. Eram estes pontos que o departamento de Arquitetura de Experiência do Usuário da Apple, onde ele trabalha, buscava melhorar. Ele também afirma que o termo hoje está sendo utilizado de forma indevida, pois desenhar uma interface não significa trabalhar na experiência do usuário. De acordo com ele, experiência do usuário “<strong>é a forma como você vê o mundo, é a forma como você experimenta sua vida, ou um serviço, ou um aplicativo</strong>”. </p>

                            <br><p>Além do termo UX, Norman disseminou termos da área bastante utilizados hoje. Entre estes, “<strong>design centrado no usuário</strong>” <em>(User Centered System Design)</em>, que é utilizado para descrever o design baseado nas necessidades do usuário, considerando estética, por exemplo, como algo secundário. O design centrado no usuário envolve simplificar a estrutura de tarefas, tornando coisas mais visíveis, criando correspondências corretas para controles e funcionalidades, explorando o poder das limitações, e projetando interfaces para suportar erros.</p>

                            <br><h4 style="color: #465564"><strong>1994 - Heurísticas de Nielsen </strong></h4>
                            <hr>

                            <br><p>É nesse momento que conhecemos o segundo nome mais importante atualmente na área: Jakob Nielsen. Em 1994, <strong>Jakob Nielsen</strong> publicou o livro <em>Engenharia de Usabilidade</em>, que traz uma definição sobre métodos de pesquisa para usuários e compila boas práticas da área.</p>
                                
                            <br><p>Em 1994, Nielsen publicou as <strong>10 heurísticas de usabilidade</strong>, ainda hoje bastante utilizadas. As heurísticas de Nielsen, como são chamadas, são a base dos critérios de boas práticas de usabilidade. Elas servem como base para criação de outras heurísticas e práticas. Apesar de não terem sido as primeiras, ao longo do tempo passaram a ser as mais populares. Hoje as heurísticas são utilizadas em vários momentos do processo de design. Junto com as heurísticas, Nielsen divulgou um método de inspeção de usabilidade que era barato, rápido e fácil de aplicar.</p>

