                            <br><br><h4 style="color: #465564"><strong>2000 - A era dos dispositivos portáteis </strong></h4>
                            <hr>

                            <br><p>Neste início de século XXI, nós vivemos a popularização dos <em>smartphones</em> e mais recentemente de dispositivos vestíveis, como relógios inteligentes. Os critérios de usabilidade tornaram-se mais especializados a cada evolução dos sistemas e ganham cada vez mais espaço no mundo da tecnologia.</p>

                            <br><p>Hoje não é mais um privilégio das grandes empresas ter um departamento ou equipe dedicada à experiência do usuário. O número de profissionais e especialistas na área de projeto de interação cresce na medida em que a demanda por produtos mais usáveis é solicitada por usuários (REBELO, 2009). Aos poucos todos se dão conta que há um retorno positivo no cuidado com a qualidade da interação.</p>

                            <br><p>Em meio a uma pandemia, percebemos o quanto os dispositivos e sistemas nos conectam e estão presentes em nosso dia a dia. Como vocês viram, ao longo da história dessa área de estudo, muitos termos e especialidades foram envolvidos para tratar do mesmo assunto: interação com interfaces de sistemas. Hoje é possível falar tanto de UX quanto da arquitetura da informação de um sistema, pois os termos que vieram de diferentes disciplinas coexistem. Essa rede complexa de disciplinas busca as soluções mais adequadas às pessoas. Como você vê o futuro dessa área?</p>

                            <br><h4 style="color: #465564"><strong>A evolução das interfaces </strong></h4>
                            <hr>

                            <br><p>Todos os fatos históricos apresentados acompanharam a evolução das interfaces computacionais. Entender de uma forma global a evolução das interfaces explica como a área cresceu e se popularizou. Segundo Fernandez (2007), a evolução das interfaces se apresenta da seguinte forma:</p>

                            <ol class="none">
                                <li class="wow fadeInRight" data-wow-delay="1.0s"><i class="fa fa-check-circle CorVerdeClaroEstado"></i> <strong>Anos 1950:</strong> Interface de hardware “para engenheiros” com diversos botões de interação;</li>
                                <li class="wow fadeInRight" data-wow-delay="1.3s"><i class="fa fa-check-circle CorVerdeClaroEstado"></i> <strong>Anos 1960-1970:</strong> Interface de programação (COBOL, FORTRAN);</li>
                                <li class="wow fadeInRight" data-wow-delay="1.6s"><i class="fa fa-check-circle CorVerdeClaroEstado"></i> <strong>Anos 1970-1990:</strong> Interface de terminais (linguagens de comando);</li>
                                <li class="wow fadeInRight" data-wow-delay="1.9s"><i class="fa fa-check-circle CorVerdeClaroEstado"></i> <strong>Anos 1980:</strong> Interface de interação para diálogo (GUIs, multimídia);</li>
                                <li class="wow fadeInRight" data-wow-delay="2.2s"><i class="fa fa-check-circle CorVerdeClaroEstado"></i> <strong>Anos 1990:</strong> Interface para realizar trabalho (redes e grupos);</li>
                                <li class="wow fadeInRight" data-wow-delay="2.5s"><i class="fa fa-check-circle CorVerdeClaroEstado"></i> <strong>A partir de 2000: </strong>Interface torna-se onipresente (Aparelhos celulares, <em>bluetooth</em>, dispositivos móveis, eletrônicos, por toda a parte, telas interativas e muitas tecnologias embarcada).</li>
                            </ol>



                            
          
